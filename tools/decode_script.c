struct CompressedHeader {
    uint32_t magic;
    uint32_t compressed;
    uint32_t decompressed;
    uint32_t version;
};

int main(int argc, char *argv[]) {
    const char *path = "/Users/victoria/IdeaProjects/umineko web/game/en.file";
    if (argc > 1) {
        path = argv[1];
    }

    FILE *fp = fopen(path, "rb");
    if (!fp) {
        fprintf(stderr, "Failed to open %s\n", path);
        return 1;
    }

    struct CompressedHeader header;
    if (fread(&header, sizeof(header), 1, fp) != 1) {
        fprintf(stderr, "Failed to read header\n");
        fclose(fp);
        return 1;
    }

    if (header.magic != CompressedMagic) {
        fprintf(stderr, "Bad magic: 0x%08X (expected 0x%08X)\n", header.magic, CompressedMagic);
        fclose(fp);
        return 1;
    }

    if (header.version != CompressedVersion) {
        fprintf(stderr, "Bad version: %u (expected %u)\n", header.version, CompressedVersion);
        fclose(fp);
        return 1;
    }

    fprintf(stderr, "Header: magic=0x%08X compressed=%u decompressed=%u version=%u\n",
            header.magic, header.compressed, header.decompressed, header.version);

    uint8_t *compressed = malloc(header.compressed);
    if (!compressed) {
        fprintf(stderr, "Failed to allocate %u bytes for compressed data\n", header.compressed);
        fclose(fp);
        return 1;
    }

    if (fread(compressed, 1, header.compressed, fp) != header.compressed) {
        fprintf(stderr, "Failed to read compressed data\n");
        free(compressed);
        fclose(fp);
        return 1;
    }
    fclose(fp);

    for (uint32_t i = 0; i < header.compressed; i++) {
        compressed[i] = CompressedConversionTable[compressed[i] ^ CompressedCrcA[0]] ^ CompressedCrcA[1];
    }

    uLongf decompressed_length = header.decompressed;
    uint8_t *decompressed = malloc(decompressed_length);
    if (!decompressed) {
        fprintf(stderr, "Failed to allocate %lu bytes for decompressed data\n", (unsigned long)decompressed_length);
        free(compressed);
        return 1;
    }

    int ret = uncompress(decompressed, &decompressed_length, compressed, header.compressed);
    free(compressed);

    if (ret != Z_OK) {
        fprintf(stderr, "Decompression failed: zlib error %d\n", ret);
        free(decompressed);
        return 1;
    }

    fprintf(stderr, "Decompressed %lu bytes successfully\n", (unsigned long)decompressed_length);

    for (uLongf i = 0; i < decompressed_length; i++) {
        decompressed[i] = CompressedConversionTable[decompressed[i] ^ CompressedCrcB[0]] ^ CompressedCrcB[1];
    }

    int max_lines = 50;
    if (argc > 2 && strcmp(argv[2], "--all") == 0) {
        max_lines = -1;
    } else if (argc > 2) {
        max_lines = atoi(argv[2]);
    }

    int lines = 0;
    for (uLongf i = 0; i < decompressed_length; i++) {
        if (max_lines >= 0 && lines >= max_lines) {
            break;
        }
        putchar(decompressed[i]);
        if (decompressed[i] == '\n') {
            lines++;
        }
    }

    free(decompressed);
    return 0;
}
